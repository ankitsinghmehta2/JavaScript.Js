// For Loop
console.log("For Loop:");
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// While Loop
console.log("\nWhile Loop:");
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}


// Do-While Loop
console.log("\nDo-While Loop:");
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);
